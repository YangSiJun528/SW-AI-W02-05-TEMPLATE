# AI 도움 받은 풀이
import sys

sys.setrecursionlimit(10**6)
input_data = sys.stdin.read().split() # read는 모든 내용을 읽음. split으로 쪼개기
n = len(input_data)
arr = [int(x) for x in input_data]
idx = 0
result = []

def bs(parent):
    global idx # input을 소비하는 느낌이므로 전역으로 관리가 깔끔
    if idx >= n: # 인덱스 범위
        return
    cur = arr[idx]
    if cur > parent: # 처리 불가능하면 종료
        return
    idx += 1
    # left: cur보다 작은 값들
    bs(cur)
    # right: cur보다 크고 parent보다 작은 값들
    bs(parent)
    # post-order: 자식 처리 후 출력
    print(cur)

bs(10**6+1)


